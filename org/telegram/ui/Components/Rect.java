package org.telegram.ui.Components;

public class Rect {
    public float height;
    public float width;
    public float f60x;
    public float f61y;

    public Rect(float x, float y, float width, float height) {
        this.f60x = x;
        this.f61y = y;
        this.width = width;
        this.height = height;
    }
}
